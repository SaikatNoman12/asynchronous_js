/* === JavaScript asynchronous ==== */


// js synchronous method 
console.log('Hello my 1st friend.');

// js asynchronous method  
setTimeout(function () {
    console.log('Hello my 2nd friend.');
}, 2000);

// js synchronous method 
console.log('Hello my 3rd friend');

// js asynchronous method
setInterval(function(){
    console.log('Hello my 4th friend.');
}, 3000)

// js synchronous method; 
console.log('Hello my 5th friend');



